#include "ft_printf.h"

int	ft_printf(const char *str, ...)
{
	va_list	args;
	int		i;
	int		len;

	i = 0;
	len = 0;
	va_start(args, str);
	while (str[i] != '\0')
	{
		if (str[i] == '%' && ft_strchr("cspdiuxX%", str[i + 1]))
		{
			len += ft_checkformat(&((char *)str)[i + 1], args);
			i++;
		}
		else
		{
			write(1, &str[i], 1);
			len++;
		}	
		i++;
	}
	va_end(args);
	return (len);
}
